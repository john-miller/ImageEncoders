package com.miller.signature.decoders;

public class ImageEncoderException extends Exception {

	private static final long serialVersionUID = 4415237219719898612L;

	public ImageEncoderException() {
		super();
	}

	public ImageEncoderException(String message, Throwable cause) {
		super(message, cause);
	}

	public ImageEncoderException(String message) {
		super(message);
	}

	public ImageEncoderException(Throwable cause) {
		super(cause);
	}

}
